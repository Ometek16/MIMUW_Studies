-- Autorstwo: Praca,Autor

WITH RECURSIVE Krawedzie AS (
	-- Wyznaczamy krawędzie grafu współautorstwa
	SELECT
  		a.Autor as Od,
  		b.Autor as Do
  	FROM Autorstwo a
  	JOIN Autorstwo b ON a.Praca = b.Praca
  	WHERE a.Autor != B.Autor
),

AutorGraf AS (
    -- Startujemy od Michała Pilipczuka
    SELECT 
        'Pilipczuk Mi'::varchar AS Autor, 
        0 AS LiczbaPilipczuka
    
    UNION
    
    -- Rozszerzamy do współautorów
    SELECT 
        DISTINCT kraw.Do AS Autor,
        ag.LiczbaPilipczuka + 1 AS LiczbaPilipczuka
    FROM AutorGraf ag
    JOIN Krawedzie kraw ON kraw.Od = ag.Autor
    WHERE ag.LiczbaPilipczuka < 7	-- Ograniczenie na długość ścieżki
),

LiczbyPilipczuka AS (
    -- Grupujemy, by każdy autor miał przypisaną minimalną liczbę Pilipczuka
    SELECT 
        Autor,
        MIN(LiczbaPilipczuka) AS LiczbaPilipczuka
    FROM AutorGraf
    GROUP BY Autor
)

-- Ostateczny wynik: wszyscy autorzy z przypisaniem liczby Pilipczuka lub NULL
SELECT 
    a.ID AS Autor,
    COALESCE(lp.LiczbaPilipczuka, NULL) AS LiczbaPilipczuka
FROM Autor a
LEFT JOIN LiczbyPilipczuka lp ON a.ID = lp.Autor
ORDER BY Autor;

